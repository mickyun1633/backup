package method;
/* 1. 두 정수를 매개변수로 받아 합을 출력하는 메소드 작성하기
		- 반환값 없음 (void)		*/
public class Method01 {
	public static void main(String[] args) {
			// 매개변수 선언
			// 정수 int
			// a에 5, b에 3 저장
			int a = 5;
			int b = 3;
			// 두 정수 연산 변수 선언
			int sum = a + b;
			// sum 결과 출력
			System.out.println("두 수의 합은 " + sum + "입니다.");
	}
}
