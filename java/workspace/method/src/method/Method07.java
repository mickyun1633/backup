package method;
// 7. 정수 하나를 매개변수로 받아 해당 수의 제곱값을 반환하는 메소드 작성하기
public class Method07 {
	public static void main(String[] args) {
		// 매개변수 선언
		// 정수 int
		// num에 10 저장
		int num = 10;
		// num의 제곱 연산 * 사용
		int square = num * num;
		// 출력
			System.out.println(num + "의 제곱값은 " + square + "입니다.");
		
	}
}
