package forTest;
//1번 : 반복문 - for문 기초1
public class ForTest01 {
	public static void main(String[] args) {
		for(int i = 0; i < 3; i++) {
//			System.out.println("김영선");
			System.out.println(i);
			
			//초기식 1번 실행
			//i = 0
			//조건식 검사
			//i < 10	0 < 10 true
			//조건식의 결과 true면 {} 실행
			//증감식 
			//i++	=> 0 + 1 => 1
			//다시 조건식 검사
			//현재 i의 값 : 1	i < 10	1 < 10 true
			//{} 실행문
			//계속 반복 조건식의 결과가 false가 되는 순간 종료			
		}
	}
}
