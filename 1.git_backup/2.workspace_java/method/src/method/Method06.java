package method;
// 6. 두 정수를 매개변수로 받아 큰 수를 출력하는 메소드 작성하기
public class Method06 {
	public static void main(String[] args) {
		// 매개변수 선언
		// 정수 int
		// num1에 5, num2에 10 저장
		int num1 = 5;
		int num2 = 10;
		// if ~ else 문 (true, false)
		// num1>num2 값이 true 이면 바로 실행
		// false 이면 else 실행
		if (num1 > num2) {
			// true이면 출력
			System.out.println(num1 + "이 큰수 입니다.");
		} else {
			// false일 때 출력
			System.out.println(num2 + "이 큰수 입니다.");
		}
	}
			
}












