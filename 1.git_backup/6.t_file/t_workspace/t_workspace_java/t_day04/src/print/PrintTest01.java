package print;
//5번 : 출력메소드(println, print)
public class PrintTest01 {
	public static void main(String[] args) {
		
		//println() : 전달된 값을 콘솔창에 출력하고 줄바꿈이 된다
		System.out.println("println 메소드입니다.");
		System.out.println("출력 후 자동으로 줄바꿈 됩니다.");
		
		//print() : 전될된 값을 콘솔창에 출력하고 줄바꿈이 되지 않는다
		System.out.print("print 메소드입니다.");
		System.out.print("줄바꿈이 되지 않는다.\n");
		
		//printf() : 형식에 맞는 값을 출력하고 줄바꿈이 되지 않는다
		
		
	}
}
